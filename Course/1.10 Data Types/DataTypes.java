public class DataTypes {
    
    public static void main(String[] args) {
        // Declare a variable of type int  // int is a 32-bit signed integer // 32-bit means 4 bytes
        int myInt = 5;
        // Declare a variable of type byte // byte is a 8-bit signed integer // 8-bit means 1 byte
        byte myByte = 127;
        // Declare a variable of type short // short is a 16-bit signed integer // 16-bit means 2 bytes
        short myShort = 32767;
        // Declare a variable of type long // long is a 64-bit signed integer // 64-bit means 8 bytes
        long myLong = 9223372036854775807L;

        // Declare a variable of type float // float is a 32-bit floating point // 32-bit means 4 bytes
        float myFloat = 3.4f;
        // Declare a variable of type double // double is a 64-bit floating point // 64-bit means 8 bytes
        double myDouble = 3.4;

        // Declare a variable of type char // char is a 16-bit unicode character // 16-bit means 2 bytes
        char myChar = 'a';
        // Declare a variable of type boolean // boolean is a true or false value // 1 bit
        boolean myBoolean = true;

        // Print the variables
        System.out.println("myInt: " + myInt);
        System.out.println("myByte: " + myByte);
        System.out.println("myShort: " + myShort);
        System.out.println("myLong: " + myLong);
        System.out.println("myFloat: " + myFloat);
        System.out.println("myDouble: " + myDouble);
        System.out.println("myChar: " + myChar);
        System.out.println("myBoolean: " + myBoolean);


    }
}
